bool CheckValidity(char board[][9], char BoardStatus[][9], int row, int col,
                   char* nextstep) {
  bool move = false;
  if (strcmp(nextstep, "Up") == 0) {
    row--;
    move = true;
  }
  if (strcmp(nextstep, "Down") == 0) {
    row++;
    move = true;
  }
  if (strcmp(nextstep, "Left") == 0) {
    col--;
    move = true;
  }
  if (strcmp(nextstep, "Right") == 0) {
    col++;
    move = true;
  }
  if (row < 0 || row > 8 || col < 0 || col > 8) {
    // false judgement
    return false;
  } else if (move) {
    return true;
  }
  if (strcmp(nextstep, "Uncover") == 0) {
    if (BoardStatus[row][col] == '0') {
      return true;
    } else {
      return false;
    }
  }
  if (strcmp(nextstep, "Flag") == 0) {
    if (BoardStatus[row][col] == '0' || BoardStatus[row][col] == 'F') {
      return true;
    } else {
      return false;
    }
  }
  return false;
}


char SearchBomb(char board[][9], char BoardStatus[][9], int row, int col) {
  char Num = '0';
  int Row = 0;
  int Col = 0;
  if (board[row][col] == 'B') {
    return 'B';
  }
  for (int i = -1; i <= 1; i++) {
    for (int j = -1; j <= 1; j++) {
      Row = row + i;
      Col = col + i;
      if (Row < 0 || Row > 8 || Col < 0 || Col > 8) {
        continue;
      } else if (board[Row][Col] == 'B') {
        Num++;
      }
    }
  }
  if (Num == '0') {
    return 'E';
  }
  return Num;
}


void SearchZero(char board[][9], char BoardStatus[][9], int row, int col) {
  if (row < 0 || row > 8 || col < 0 || col > 8) {
    return;
  }
  int Row = 0;
  int Col = 0;
  for (int i = -1; i <= 1; i++) {
    for (int j = -1; j <= 1; j++) {
      Row = row + i;
      Col = col + i;
      if (Row < 0 || Row > 8 || Col < 0 || Col > 8) {
        continue;
      } else if (board[Row][Col] == 'B') {
        return;
      }
    }
  }
  for (int i = -1; i <= 1; i++) {
    for (int j = -1; j <= 1; j++) {
      if (i != 0 || j != 0) {
        SearchZero(board, BoardStatus, row + i, col + j);
      }
    }
  }
  BoardStatus[row][col] = 'E';  // Empty bomb in the Vicinity
  return;
}


bool GameEnd(char board[][9], char BoardStatus[][9], char ChoseData) {
  int BombLEFT = 0;
  int BombNum = 0;
  if (ChoseData == 'B') {
    return true;
  }
  for (int i = 0; i <= 8; i++) {
    for (int j = 0; j <= 8; j++) {
      if (BoardStatus[i][j] == '0' || BoardStatus[i][j] == 'F') {
        BombLEFT++;
      }
      if (board[i][j] == 'B') {
        BombNum++;
      }
    }
  }
  if (BombNum == BombLEFT) {
    return true;
  } else {
    return false;
  }
}

