void vsyncWait() {
    volatile int * pixel_ctrl_ptr = (int *)0xFF203020;
    *pixel_ctrl_ptr = 1;
    int status = *(pixel_ctrl_ptr + 3) & 1;
    while (status != 0) {
        status = *(pixel_ctrl_ptr + 3) & 1;
    }
}

void animations(bool end) {
    extern const uint16_t left[213][160];
    extern const uint16_t right[216][160];

    short int Buffer1[240][512]; // 240 rows, 512 (320 + padding) columns
    short int Buffer2[240][512];

    volatile int * pixel_ctrl_ptr = (int *)0xFF203020;

    *(pixel_ctrl_ptr + 1) = (int) &Buffer1; 

    vsyncWait();

    pixel_buffer_start = *pixel_ctrl_ptr;
    clear_screen(); 

    *(pixel_ctrl_ptr + 1) = (int) &Buffer2;
    pixel_buffer_start = *(pixel_ctrl_ptr + 1); 
    clear_screen(); 


    int x_left = -160; 
    int x_right = 830; 
	  int count = 0;
	  int speed = 6;
	  int y_offset = 11;

    while (end) {
        for (int y = 0; y < 216; y++) {
            for (int x = 0; x < 160; x++) {
                plot_pixel(x + x_left, y+y_offset, left[y][x]);
            }
        }


        for (int y = 0; y < 216; y++) {
            for (int x = 0; x < 160; x++) {
                plot_pixel(x+x_right , y+y_offset-1, right[y][x]);
            }
        }

        x_left+=speed;  
        x_right-=speed; 
		count++;
		//printf("\n%d",count);

        vsyncWait();

        pixel_buffer_start = *(pixel_ctrl_ptr + 1); 
        
		if (count > 27) {
            break;
		}
	}
}


